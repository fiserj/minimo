static void setup(void)
{
    title("Custom Shader Example");

    clear_color(0x333333ff);
    clear_depth(1.0f);

    create_shader(SHADER_ID, VS, sizeof(VS), FS, sizeof(FS));

    create_uniform(COLOR_ID, UNIFORM_VEC4, 1, "u_color");
}

static void hsv2rgba(float h, float s, float v, float* rgba);

static void draw(void)
{
    if (key_down(KEY_ESCAPE))
    {
        quit();
    }

    identity();
    ortho(-aspect(), aspect(), -1.0f, 1.0f, 1.0f, -1.0f);
    projection();

    identity();
    rotate_z((float)elapsed() * -50.0f);

    begin_mesh(TRIANGLE_ID, MESH_TRANSIENT);
    {
        vertex(-0.6f, -0.4f, 0.0f);
        vertex( 0.6f, -0.4f, 0.0f);
        vertex( 0.0f,  0.6f, 0.0f);
    }
    end_mesh();

    shader(SHADER_ID);

    float rgba[4];
    hsv2rgba((float)elapsed() * 120.0f, 1.0f, 1.0f, rgba);
    uniform(COLOR_ID, rgba);

    identity();
    mesh(TRIANGLE_ID);
}

void hsv2rgba(float h, float s, float v, float* rgba)
{
    // https://rosettacode.org/wiki/Color_wheel#C.2B.2B

    const float q = fmodf(h, 360.0f) / 60.0f;
    const float c = s * v;
    const float x = c * (1.0f - fabs(fmodf(q, 2.0f) - 1.0f));
    const float m = v - c;

    switch ((int)q)
    {
    case 0:
        rgba[0] = c;
        rgba[1] = x;
        rgba[2] = 0.0f;
        break;
    case 1:
        rgba[0] = x;
        rgba[1] = c;    
        rgba[2] = 0.0f;
        break;
    case 2:
        rgba[0] = 0.0f;
        rgba[1] = c;
        rgba[2] = x;
        break;
    case 3:
        rgba[0] = 0.0f;
        rgba[1] = x;
        rgba[2] = c;
        break;
    case 4:
        rgba[0] = x;
        rgba[1] = 0.0f;
        rgba[2] = c;
        break;
    default:
        rgba[0] = c;
        rgba[1] = 0.0f;
        rgba[2] = x;
    }

    rgba[0] += m;
    rgba[1] += m;
    rgba[2] += m;
    rgba[3]  = 1.0f;
}

MNM_MAIN(0, setup, draw, 0);
